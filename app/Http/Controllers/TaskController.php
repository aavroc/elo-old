    public function show(Request $request)
    {
        $name = $request->repo;
        $path = $request->path;
        $repo = Module::where('name', $name)->first();
        $github = new GitHub();

        $data = [];
        if ($path == "README.md") {
            $file_data = $github->get_readme($repo->name);
            $content = base64_decode($file_data->content);
            $data['content'] =  $content;
            $data['repo'] =  $repo->name;
        } else {


        }

        return view('tasks.show', $data);
    }

